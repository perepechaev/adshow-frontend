<p>7.4. Клиент несет всю ответственность за соблюдение всех требований действующего законодательства РФ, в том числе законодательства РФ о рекламе, о защите авторских и смежных прав, об охране товарных знаков и знаков обслуживания.</p>

<p>7.5. Клиент несет ответственность за достоверность сведений, указанных им в рекламном объявлении.</p>

